package Introduction.DataType;

public class DataType {
    public static void main(String[] args) {
        byte x = 3;
        int y = 2;
        float a = 3.4f;
        double b = 2.333333;
        System.out.println(a);
        System.out.println(b);
        System.out.println(x);
        System.out.println(y);
    }
}